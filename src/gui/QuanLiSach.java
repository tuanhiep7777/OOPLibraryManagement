    private void tableSachMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tableSachMouseClicked
        // TODO add your handling code here:
        int row = tableSach.getSelectedRow();
        String MaS = (tableSach.getModel().getValueAt(row, 0)).toString();
        SachDTO newDTO = null;
        try {
            newDTO = SachDAO.LoadRowContent(MaS);
        } catch (SQLException ex) {
            Logger.getLogger(QuanLiSach.class.getName()).log(Level.SEVERE, null, ex);
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(QuanLiSach.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        if (newDTO == null){
        }
        else{
            txtMaS1.setText(newDTO.getMaSach());
            txtMaSDeXoa.setText(newDTO.getMaSach());
            txtTenS1.setText(newDTO.getTenSach());
            txtTenTG1.setText(newDTO.getTenTacGia());
            txtTheLoai1.setText(newDTO.getTheLoai());
            txtNXB1.setText(newDTO.getNXB());
            txtNamXB1.setText(Short.toString(newDTO.getNamXuatBan()));
            txtDonGia1.setText(Integer.toString(newDTO.getDonGia()));
            txtSL1.setText(Short.toString(newDTO.getSoLuong()));
        }
    }//GEN-LAST:event_tableSachMouseClicked

