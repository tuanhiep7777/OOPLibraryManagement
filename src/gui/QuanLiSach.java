    private void buttThemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttThemActionPerformed
        // TODO add your handling code here:
        String strMaS = txtMaS.getText();
        if (isNull(txtMaS, strMaS, "Mã sách")) return;
        if (SachDAO.isIDExistence(strMaS)){
            JOptionPane.showMessageDialog(null, "Mã sách " + strMaS + " đã có trong hệ thống");
            txtMaS.requestFocus();
            return;
        }
        
        String strTenS = txtTenS.getText();
        if (isNull(txtTenS, strTenS, "Tên sách")) return;
        
        String strTenTG = txtTenTG.getText();
        if (isNull(txtTenTG, strTenTG, "Tên tác giả")) return;
        
        String strTheLoai = txtTheLoai.getText();
        if (isNull(txtTheLoai, strTheLoai, "Thê loại")) return;
        
        String strNXB = txtNXB.getText();
        if (isNull(txtNXB, strNXB, "NXB")) return;
        
        String strNamXB = txtNamXB.getText();
        if (isNull(txtNamXB, strNamXB, "Năm xuất bản")) return;
        try{
            Short.parseShort(strNamXB);
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(null, "Giá trị '" + strNamXB + "' không hợp lệ!");
            txtNamXB.requestFocus();
            return;
        }
        
        String strDonGia = txtDonGia.getText();
        if (isNull(txtDonGia, strDonGia, "Đơn giá")) return;
        try{
            Integer.parseInt(strDonGia);
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(null, "Giá trị '" + strDonGia + "' không hợp lệ!");
            txtDonGia.requestFocus();
            return;
        }
        
        String strSL = txtSL.getText();
        if (isNull(txtSL, strSL, "Số lượng")) return;
        try{
            Short.parseShort(strSL);
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(null, "Giá trị '" + strSL + "' không hợp lệ!");
            txtSL.requestFocus();
            return;
        }
        
        SachDTO newBook = new SachDTO(strMaS, strTenS, strTenTG, strTheLoai, strNXB, Short.parseShort(strNamXB), Integer.parseInt(strDonGia), Short.parseShort(strSL));
        
        try {
            SachDAO.Them(newBook);
            JOptionPane.showMessageDialog(null, "Thêm thành công!");
            SetModelToCombobox();
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(QuanLiSach.class.getName()).log(Level.SEVERE, null, ex);
            JOptionPane.showMessageDialog(null, "Thêm thất bại!");
        } catch (SQLException ex) {
            Logger.getLogger(QuanLiSach.class.getName()).log(Level.SEVERE, null, ex);
            JOptionPane.showMessageDialog(null, "Thêm thất bại!");
        }
        
        Load_RefreshTable();
    }//GEN-LAST:event_buttThemActionPerformed

