    private void jLabel4MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jLabel4MouseClicked
        // TODO add your handling code here:
        String MaMT = txtMaMT.getText().trim();
        String MaDG = txtMaDG.getText().trim();
        String MaNV = txtMaNV.getText().trim();
        String NgayMuon = txtNgayMuon.getText().trim();
        String HanTra = txtHanTra.getText().trim();
        
        String TienCoc = txtTienCoc.getText().trim();
        if (!TienCoc.equals("")) {
            try {
                Integer.parseInt(TienCoc);
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, "Tiền cọc không hợp lệ!");
                txtTienCoc.requestFocus();
                return;
            }
        }
        
        String TienPhat = txtTienPhat.getText().trim();
        if (!TienPhat.equals("")) {
            try {
                Integer.parseInt(TienPhat);
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, "Tiền phạt không hợp lệ!");
                txtTienPhat.requestFocus();
                return;
            }
        }
        
        try {
            MuonTraDAO.Search_Load_Table(tableMT, MaMT, MaDG, MaNV, NgayMuon, HanTra, TienCoc, TienPhat);
        } catch (ClassNotFoundException | SQLException ex) {
            Logger.getLogger(GUITimKiemChung.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jLabel4MouseClicked

