            try {
                Long.parseLong(strCMND);
            } catch (NumberFormatException e) {
                JOptionPane.showMessageDialog(null, "Giá trị '" + strCMND + "' không hợp lệ!");
                txtcmnd.requestFocus();
                return;
            }

            String strNS = txtngays.getText();
            if (isNull(txtngays, strNS, "Ngày sinh")) {
                return;
            }
            try {
                Date.valueOf(strNS);
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, "Giá trị '" + strNS + "' không hợp lệ!");
                txtngays.requestFocus();
                return;
            }

            String strDC = txtdiachi.getText();
            if (isNull(txtdiachi, strDC, "Địa chỉ")) {
                return;
            }

            String strDT = txtsdt.getText();
            if (isNull(txtsdt, strDT, "Số điện thoại")) {
                return;
            }
            try {
                Long.parseLong(strDT);
            } catch (NumberFormatException e) {
                JOptionPane.showMessageDialog(null, "Giá trị '" + strDT + "' không hợp lệ!");
                txtsdt.requestFocus();
                return;
            }

            String strEM = txtemail.getText();

            String strTK = txttaikhoan.getText();
            if (isNull(txttaikhoan, strTK, "Số tiền trong tài khoản")) {
                return;
            }

