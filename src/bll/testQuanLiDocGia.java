    private void TieuChiItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_TieuChiItemStateChanged
        // TODO add your handling code here:
        if (evt.getStateChange() == ItemEvent.SELECTED) {

            if (TieuChi.getSelectedItem().equals("Mã độc giả")) {
                TieuChiTK = "MA_DOC_GIA";
            } else if (TieuChi.getSelectedItem().equals("Tên độc giả")) {
                TieuChiTK = "TEN_DOC_GIA";
            } else if (TieuChi.getSelectedItem().equals("Giới tính")) {
                TieuChiTK = "GIOI_TINH";
            } else if (TieuChi.getSelectedItem().equals("CMND")) {
                TieuChiTK = "CMND";
            } else if (TieuChi.getSelectedItem().equals("Ngày sinh")) {
                TieuChiTK = "NGAY_SINH";
            } else if (TieuChi.getSelectedItem().equals("Địa chỉ")) {
                TieuChiTK = "DIA_CHI";
            } else if (TieuChi.getSelectedItem().equals("Số điện thoại")) {
                TieuChiTK = "SO_DIEN_THOAI";
            } else if (TieuChi.getSelectedItem().equals("Email")) {
                TieuChiTK = "EMAIL";
            } else if (TieuChi.getSelectedItem().equals("Tài khoản")) {
                TieuChiTK = "TAI_KHOAN";
            } else if (TieuChi.getSelectedItem().equals("Tiền phạt")) {
                TieuChiTK = "TIEN_PHAT";
            } else {
                TieuChiTK = "ALL";
            }
        }
    }//GEN-LAST:event_TieuChiItemStateChanged

