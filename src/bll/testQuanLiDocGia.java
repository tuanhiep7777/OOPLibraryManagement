    private void tableDGMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tableDGMouseClicked
        // TODO add your handling code here:
        int row = tableDG.getSelectedRow();
        String MaDG = (tableDG.getModel().getValueAt(row, 0)).toString();
        DocGiaDTO newDTO = null;
        try {
            newDTO = DocGiaDAO.LoadRowContent(MaDG);
        } catch (SQLException ex) {
            Logger.getLogger(testQuanLiDocGia.class.getName()).log(Level.SEVERE, null, ex);
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(testQuanLiDocGia.class.getName()).log(Level.SEVERE, null, ex);
        }

        if (newDTO == null) {
        } else {
            txtmadg.setText(newDTO.getMaDG());
            txttendg.setText(newDTO.getTenDG());
            txtgt.setText(newDTO.getGioiTinh());
            txtcmnd.setText(Long.toString(newDTO.getCMND()));
            txtngays.setText(newDTO.getNgaySinh().toString());
            txtdiachi.setText(newDTO.getDiaChi());
            txtsdt.setText(Long.toString(newDTO.getSDT()));
            txtemail.setText(newDTO.getEmail());
            txttaikhoan.setText(Long.toString(newDTO.getTaiKhoan()));
            txttienphat.setText(Long.toString(newDTO.getTienPhat()));
        }
    }//GEN-LAST:event_tableDGMouseClicked

